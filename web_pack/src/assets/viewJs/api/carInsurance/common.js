//添加cookie oneCarOrderConfirm记录这些参数 方便调用
function setOrderConfirm(json){
    //苹果 手机上 不支持 $.cookie('oneCarOrderConfirm');写法 调用 localStorage存储
    var mb = myBrowser();
    //alert('aa');
    var aa = mb ? $.cookie('oneCarOrderConfirm') : localStorage.getItem('oneCarOrderConfirm');
    //if(!json) return false;
    //如果有cookie oneCarOrderConfirm 就遍历修改 没有就直接保存
    if(aa){
        var oneCarOrderConfirm = mb ? JSON.parse($.cookie('oneCarOrderConfirm')) || {} : JSON.parse(localStorage.getItem('oneCarOrderConfirm')) || {};
        for(var aa in json){
            oneCarOrderConfirm[aa] = json[aa];
        }
        mb ? $.cookie('oneCarOrderConfirm', JSON.stringify(oneCarOrderConfirm), { expires: 1, path: '/',raw: true })
        : localStorage.setItem('oneCarOrderConfirm',JSON.stringify(oneCarOrderConfirm));
    }else{
        mb ? $.cookie('oneCarOrderConfirm', JSON.stringify(json), { expires: 1, path: '/',raw: true }) : localStorage.setItem('oneCarOrderConfirm',JSON.stringify(json));;
    }
}
